package com.safenetinc.viewpin.simulator.authority.exceptions;

/**
 * Exception to be thrown when an error associated with the PINRetrievalResponse occurs
 * 
 * @author Stuart Horler
 * 
 * 
 */
public class PinRetrievalResponseException extends Exception
{
    private static final long serialVersionUID = 42L;

    /**
     * Constructor
     */
    public PinRetrievalResponseException()
    {
        super();
    }

    /**
     * Constructor
     * 
     * @param message The message associated with this exception
     */
    public PinRetrievalResponseException(String message)
    {
        super();
    }
}
