package com.safenetinc.viewpin.simulator.authority.exceptions;

/**
 * Exception to be thrown when an error associated with a PINRetrievalRequest occurs
 * 
 * @author Stuart Horler
 * 
 * 
 */
public class PinRetrievalRequestException extends Exception
{
    private static final long serialVersionUID = 42L;

    /**
     * Constructor
     */
    public PinRetrievalRequestException()
    {
        super();
    }

    /**
     * Constructor
     * 
     * @param message The message associated with this exception
     */
    public PinRetrievalRequestException(String message)
    {
        super();
    }
}
